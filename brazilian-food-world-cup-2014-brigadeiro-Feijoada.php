                          <h1>Delicious Brazilian Food</h1>
                          <p>For all soccer fans that are going be following the Brazilian World Cup 2014 here are some tips about typical Brazilian food that can also be found here in Toronto.
                               </p>
                          <ul>
                              
                              <li>
                              <strong>Feijoada</strong> also well known as Brazil national dish, is a stew of beans with beef and pork. Portuguese feijoada is usually served with rice and assorted sausages, such as chouriço, morcela (blood sausage), farinheira, and others, which may or may not be cooked in the stew.<br/>

                              </li>
                              
                              <li>
                                  <strong>Farofais</strong> a delicious accompaniment to many Brazilian dishes, especially the classic black bean stew feijoada. Farofa is made with manioc flour (mandioca flour), which is toasted in a skillet with butter, onions and palm oil. The root of the manioc (cassava) plant is commonly sold as a very fine starch (almidón), often called tapioca flour in North America.<br/>

                              </li>
                              
                              <li>
                                  For the fish lover <strong>Brazilian Moqueca</strong>, a fish stew made with firm white fish, onions, garlic, bell peppers, tomatoes, cilantro, and coconut milk. Brazilians like to add hot sauce to make it sweet, spicy and delicious.<br/>

                              </li> 
                             
                              <li>
                                While most self-respecting soccer fans will drink beer the majority of the day be sure to try, <strong>Caipirinha</strong>, the national cocktail of Brazil. Made with Cachaça (sugar-cane rum), sugar and lime, it is sure to refresh on a hot summer day.<br/>
                              </li>
                          
                               <li>
                                   <strong>Brigadeiro</strong> (also known in some southern Brazilian states as negrinho) is a simple Brazilian chocolate bonbon made by mixing sweetened condensed milk, butter and cocoa powder. It is a very popular candy in Brazil and it is usually served at birthday parties, but can also be found at any kind of party.<br/>
                               </li>
                               </ul>
                               
                               
                               <h4>Brigadeiro's Recipe:</h4>
                               
                               <ul>
                                   <li>
                                       1 (14 ounce) can sweetened condensed milk
                                   </li>
                                   
                                   <li>
                                       1 tablespoon butter
                                   </li>
                                   
                                   <li>
                                       3 tablespoons cocoa
                                   </li>
                                   
                                   <li>
                                      chocolate sprinkles 
                                   </li>
                                   
                                   <li>
                                      <h5>Directions:</h5>
                                
                                       <ul>
                                           <li>
                                                Over medium-low heat, stir vigorously the three ingredients.
                                           </li>

                                           <li>
                                            Cook the mixture until it thickens enough to show the pan bottom during stirring (it will be firm enough to stay together and not spread) Pour into a greased dish and let cool to room temperature (or chill a few minutes in the fridge) Grease your hands with butter and take small amounts of candy and form into 1 1/2 inch balls.

                                           </li>

                                           <li>
                                             Roll the balls in chocolate jimmies to decorate.

                                           </li>

                                           <li>
                                               If the balls don't hold their shape, cook an additional 5 minutes until it thickens up more.
                                           </li>
                                       </ul>
                                   </li>
                               </ul>         
                                

                              
