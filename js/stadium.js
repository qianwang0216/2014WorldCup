
        var currslid = 0;
        function setfoc0(id){
            document.getElementById("focpic0").src = picarry[id];
            document.getElementById("foclnk0").href = lnkarry[id];
            document.getElementById("fttltxt0").innerHTML = '<a href="'+lnkarry[id]+'" target="_blank">'+ttlarry[id]+'</a>';
            currslid = id;
            for(i=0;i<6;i++){
                document.getElementById("tmb0_"+i).className = "thubpic0";
            };
            document.getElementById("tmb0_"+id).className ="thubpiccur0";
            //focpic.style.visibility = "hidden";
            focpic0.filters[0].Apply();
            if (focpic0.style.visibility == "visible") {
                focpic0.style.visibility = "hidden";
            }
          
        }
