                          <h1>Places you should not miss at Sao Paulo</h1>
                          <p>
                            Sao Paulo, one of the host cities of the 2014 FIFA World Cup, is the largest city of Brazil. A new stadium has been built in the cityâ€™s Eastern Zone to host the Opening Match (Brazil VS Croatia). Totally 6 matched will be held in this new stadium which has a capacity of 68, 000.
