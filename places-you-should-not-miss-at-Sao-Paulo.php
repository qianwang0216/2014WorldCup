                          <p>
                            If you come to Sao Paulo during the World Cup, these are the attractions you should not miss.  
                          </p>
                          <p>
                              <strong>The Julio Prestes Cultural Center - Sala Sao Paulo</strong>
                              <br />
                              The cultural center is located in the Julio Prestes Train Station in the old north central section of the city. The old station is rebuilt to an amazing convert hall. This is a special place for classical music and architecture lovers. The acoustics of the place is stunning. Please check their event calendar before you visit and reserve a seat to enjoy a magical concert. If your schedule does not match, it is even fantastic for a simple visit without a concert.
                              <br />
                              It can be easily accessed from subway, taxi or car but subway is preferred.
                          </p>
                          <p>
                              <strong>Paulista Museum</strong>
                              <br />
                              This is a Brazilian history museum located in the southeast region of the city. It was built between 1885 and 1890 as a monument to the proclamation of Brazil's independence from Portugal. With its historic building, the museum features over 125,000 items, including paintings, sculptures, photographs, maps, clothing, objects and archives.
                              <br />
                              The outdoor garden is one of the most quiet place in Sao Paulo. The best way for visitor to come here is by taxi since no subway in this area.
                          </p>
                          <p>
                              <strong>San Paulo See Metropolitan Cathedral</strong>
                              <br />
                              This neo-gothic style cathedral began in 1913 and ended four decades later. It is now the largest church in Sao Paulo and considered by some to be the 4th largest neo-gothic cathedral in the world. 
                              <br />
                              The visitor can access the Cathedral by subway. Please be aware there are a lot of homeless people at the front square. It is suggested to visit there during the day when police trailers are open and avoid using any electronic device.
                          </p>
