            <h1 class="shopping_title">5 great things you must buy when you visit Brazil!</h1>
            <p>
            The 2014 FIFA world cup is around the corner and people all over the world would visit Brazil soon. As many tourists love shopping, I am going to introduce 5 great items you must buy when you travel to Brazil today.
            <br>
            </p>
            <p>
                <strong>1) Havaianas</strong>
            <br>
            Does anyone know Havaianas is actually from Brazil, not Hawaii? Havaianas is the famous brand of flip-flop sandals and you can buy the pairs cheaply here in Brazil. For instance, you can buy the pairs with a simple design around 10 R$ (around 5 C$). You can also customize your own pair to make the souvenir extra special.
            </p>
            
            <p>
                <strong>2) Cangas</strong>
            <br>
            The cangas is the Portuguese word for a sarong. While the cangas is most known as beachwear or to sit on the sand on a beach, you can also throw over a bedspread or chair to add some extra flair. The most frequently asked question is "How do you tie a canga?" Here are the steps to tie the canga to wear as a skirt.
            <br><br>
            1) Wrap canga evenly around your waist, pulling the ends forward. If you have a fringed canga, let the unfringed end be the one you pull around your waist.
            <br>
            2) Tuck one end into your bathing suit bottom at the opposite hip. Secure with a pin if the elastic of your suit or string of your bikini is not enough to hold fabric. 
            <br>
            3) Bring free end to the other hip, smoothing the fabric flat across your belly. Hold the fabric with one hand and begin to fold a few inches past your belly button. 
            <br>
            4) Make three pleats starting at four inches in length and decreasing to two inches.
            <br>
            5) Fold pleated end into waistband. 

            </p>
            
            <p>
                <strong>3) Brazilian Coffee</strong>
            <br>
            It goes without saying that Brazil is the world’s largest producer of coffee and it has over 150 years history. The price range is R$2 over but R$5 would be best to buy. Pick up a delicious bag of beans from one of the popular cafés like Suplicy or Santo Grão. It would never disappoint you.
            </p>
            
            <p>
                <strong>4) Brazilian Hammock</strong>
            <br>
            If someone has the enough space, you can try a traditional Brazilian hammock. More durable than your standard string type, this makes for a very useful and relaxing present. Just be sure that where you are attaching it is extremely secure.
            </p>
            
            <p>
                <strong>5) Misanga</strong>
            <br>
            Misanga is an international good luck charm made from knotted embroidery floss, thread or gimp. It originates in Salvador, Bahia, Brazil. Similar to friendship bracelets, it is made with basic knots as well as patterning techniques. Its basic structure is a three thread plaited braid. It is becoming a popular portable craft project. People use them to make a wish and many soccer players wear them as good luck charms. In Brazil, the price is around 4 R$. 
            </p>
            <p>
                I hope this information helps for you to choose souvenirs! Have a safe trip to Brazil.
